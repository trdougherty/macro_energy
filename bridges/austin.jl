# ╔═╡ 78ffccf8-c768-4012-a724-cf61b819e425
begin
n_buildings = "ECAD Number of Buildings"
energy_filt = filter( x -> x[n_buildings] == 1, dropmissing(energy, n_buildings) )
energy_filt[:,"area"] = energy_filt[:,"ECAD Rentable sqft"] .* 0.092903
energy_filt[:,"energy"] = energy_filt[:,"Yearly kWh"] ./ 1000
energy_filt[:,"footprint_id"] = string.(hash.(energy_filt[:,"Property ID #"]))

energyₖₒ = dropmissing!(energy_filt, ["footprint_id","energy","area"])
end

# ╔═╡ db7f7e7f-28e2-403d-8e8a-a90ba5d3684f
select(energy_filt, ["footprint_id","energy","area"])

# ╔═╡ dca1a018-496c-493c-a8bc-3f627b8c2a39
names(energy_filt)

# ╔═╡ 7bbdbb6f-96bf-452e-9a3f-3eb829315482
footprints_file = joinpath(city, "footprints.geojson")

# ╔═╡ 19adb84d-c315-49f9-93cd-cb99a48b3fc4
footprints = GeoDataFrames.read(footprints_file)

# ╔═╡ a9802ea8-cfcf-4725-8095-7ff3ca1a2692
footprints[:,"id"] = string.(hash.(footprints.building_footprints_2013_id));

# ╔═╡ 100935d9-87a0-47a2-bb51-fb6a846dc466
names(footprints)

# ╔═╡ 02051b17-8c55-4368-ab77-1ccb3ce21e43
begin
connected_data = innerjoin(
	footprints,
	energyₖₒ,
	on = :id => :footprint_id
);
connected_data[:,"year"] = repeat([2019], nrow(connected_data))
end;

# ╔═╡ 038ee4c3-c171-49b3-87f5-afa9da4f3963
footprintsₖ = select(connected_data, [:id, :geometry])

# ╔═╡ 4264e9cc-8194-48b8-a0de-c7bc58f9f310
GeoDataFrames.write(joinpath(output_dir, "footprints.geojson"), footprintsₖ; geom_column=:geometry, driver="GeoJSON")

# ╔═╡ 455c96db-7db4-4d9e-b9b4-fe536fb0717b
begin
energyₖ = select(connected_data, [:id, :year, :energy, :area])
rename!(energyₖ, :id => :footprint_id)
end

# ╔═╡ 7e59761e-2f95-4ed7-aa6b-3e5462277576
