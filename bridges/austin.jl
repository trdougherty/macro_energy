output_dir = joinpath(city, "prepped")

# ╔═╡ 29303378-147d-4c69-af7e-9854508ec44a
mkpath(output_dir)

# ╔═╡ 24f27c3e-408f-480d-a046-3ff6fb050ca3
energy = CSV.read(joinpath(city, "energy.csv"), DataFrame)

# ╔═╡ 40e5aea8-2a13-4b51-aec8-f214d422778a
rename(
	energy,
	"Yearly kWh" => "energy"
